- (int)getNumberSection
{
    int numSec = 0;
    
    if ([_contactInfo checkTel]) {
        numSec++;
    }
    if ([_contactInfo checkEMail]) {
        numSec++;
    }
    if ([_contactInfo checkWebPage]) {
        numSec++;
    }
    if ([_contactInfo checkAddress]) {
        numSec++;
    }
    if ([_contactInfo checkData]) {
        numSec+=2;
    }
    
    return numSec;
}

